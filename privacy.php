<h1>iChat Privacy Policy</h1>

    <h2>1. Information We Collect</h2>
    <p>We collect the following information:</p>

    <h3>1.1 Personal Information</h3>
    <ul>
        <li>When you sign up for iChat, we collect your name, email address, phone number, username, and profile image.</li>
    </ul>

    <h3>1.2 Content and Activities</h3>
    <ul>
        <li>Communications: We store iChat communications sent and received. These include text as well as images and videos.</li>
    </ul>

    <h2>2. How We Use Your Information</h2>
    <ul>
        <li>Maintain and improve the iChat platform and services.</li>
        <li>Personalize your experience by recommending friends or content.</li>
        <li>Inform you about changes, features, and marketing.</li>
        <li>Protect your account against fraud.</li>
        <li>Analyze user activities to improve platform speed and features.</li>
    </ul>

    <h2>3. How We Share Your Information</h2>
    <p>We may share your information with:</p>
    <ul>
        <li>Third-Party Service Providers: We collaborate with partners to provide data hosting, analytics, and customer support. These third parties are bound to keep your information secure.</li>
    </ul>

    <h2>4. Account Control</h2>
    <p>You have control over your privacy settings:</p>
    <ul>
        <li>Delete Account: You can permanently delete your account and data. Note that some information may remain in backups for a limited period.</li>
        <li>Security: You are allowed to change your password to ensure privacy.</li>
    </ul>